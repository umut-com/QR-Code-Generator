class CardModel {
  final String name;
  final String surname;
  final String title;
  final String email;
  final String phone;
  final String workPhone;
  final String fax;
  final String company;
  final String workAddress;
  

  CardModel({
    required this.name,
    required this.surname,
    required this.title,
    required this.email,
    required this.phone,
    required this.workPhone,
    required this.fax,
    required this.company,
    required this.workAddress
  });
}
